namespace Morvan\Bundle\RespondersBundle\Tests\TestObject;

/**
 * Class OutputTest
 */
class OutputTest
{
    /** @var int */
    protected $id;

    /** @var string */
    protected $name;

    /**
     * OutputTest constructor.
     *
     * @param int $id
     * @param     $name
     */
    public function __construct(
        int $id,
        string $name
    ) {
        $this->id = $id;
        $this->name = $name;
    }

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}
